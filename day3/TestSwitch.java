package com.hqyj.cl.day3;

public class TestSwitch {
    public static void main(String[] args) {
        int day = 25;
        switch (day){
            case 23:
                System.out.println(23);

            case 25:
                System.out.println(25);
                break;
            case 24:
                System.out.println(24);
            default:
                System.out.println("哈哈哈哈哈");
        }
    }
}
